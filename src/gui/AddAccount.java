    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (validData() != "") {
            JOptionPane.showMessageDialog(this, validData(), "KIỂN TRA DỬ LIỆU", JOptionPane.YES_NO_OPTION);
        } else {
            //luu database
            AccountDAO dao = new AccountDAO();
            AccountDTO acc = new AccountDTO(txtUser.getText(), new String(txtPass.getPassword()));

            //kiem tra username da ton tai hay chua
            if (dao.isExist(acc)) {
                JOptionPane.showMessageDialog(this, "TÀI KHOẢN NÀY ĐÃ TỒN TẠI", "THÔNG BÁO", JOptionPane.YES_NO_OPTION);
            } else {
                if (dao.create(acc)) {
                    JOptionPane.showMessageDialog(this, "ĐÃ LƯU TÀI KHOẢN", "LƯU THÔNG TIN", JOptionPane.YES_NO_OPTION);
                    //xoa trong cac truong
                    txtUser.setText("");
                    txtPass.setText("");
                    txtRePass.setText("");
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

