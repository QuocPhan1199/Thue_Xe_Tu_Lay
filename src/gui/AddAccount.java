    String validData() {
        String mess = "";
        if (txtUser.getText().length() == 0) {
            mess += "Username KHÔNG ĐƯỢC ĐỂ TRỐNG";
        }
        if (!new String(txtPass.getPassword()).equals(new String(txtRePass.getPassword()))) {
            mess += "Pass AND repass CHƯA KHỚP";
        }
        return mess;
    }

